#Tuple Functions - len(),max(),min(),sum(),sorted(),list(),any(),all(),tuple()
#len()
my_tuple = (3,1,2)
print(len(my_tuple))

#max()
print(max(my_tuple))

#min()
print(min(my_tuple))

#sum()
print(sum(my_tuple))

#sorted()
print(sorted(my_tuple))

#tuple()
my_list = [1,2,3]
print(tuple(my_list))

#any()

print(any((1,"")))

#all
print(all(('','','1')))


#Tuple Methods - index(),count()

#index
my_tuple = (1,2,3,4,3,5,2)
print(my_tuple.index(3))

#count
print(my_tuple.index(3))
